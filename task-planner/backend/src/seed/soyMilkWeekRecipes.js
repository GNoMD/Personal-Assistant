function recipe({
  day,
  title,
  totalG,
  focus,
  ingredients,
  tags,
}) {
  return {
    title: `${title}（${totalG}g）`,
    mealType: '饮品',
    series: '豆浆轮换',
    templateKey: `soy-week-d${day}`,
    prepMinutes: 25,
    calories: null,
    tags: `豆浆轮换,周${['一', '二', '三', '四', '五', '六', '日'][day - 1]},${tags}`,
    ingredients: ingredients.map((line) => line).join('\n'),
    steps: [
      '【前一晚或提前4小时】按上方干料称重，清水浸泡，倒掉泡水并沥干。',
      '【制作】干料 + 清水约1200ml（4人份），打至细腻并煮熟；保留豆渣，不加糖。',
      '【饮用】约40℃温热分饮；睡前2小时勿饮。',
    ].join('\n'),
    notes: [
      `功效：${focus}`,
      '',
      SOY_WEEK_COMMON_RULES,
    ].join('\n'),
  };
}

