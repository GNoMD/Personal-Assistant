function recipe({
  day,
  title,
  totalG,
  focus,
  ingredients,
  tags,
  extraNotes = '',
}) {
  return {
    title: `${title}（${totalG}g）`,
    mealType: '早餐',
    series: '豆浆早餐',
    planDay: day,
    templateKey: `soy-breakfast-d${day}`,
    prepMinutes: 25,
    calories: null,
    tags: `豆浆早餐,一人份,周${['一', '二', '三', '四', '五', '六', '日'][day - 1]},${tags}`,
    ingredients: ingredients.join('\n'),
    steps: [
      '【前一晚】按上方干料称重；豆类/杂粮与山药茯苓莲子百合芡实分别用凉白开浸泡 ≥4 小时；燕麦片泡 2～4 小时。',
      '【早晨】倒掉全部泡水并沥干；黑芝麻/核桃/花生洗净即可。',
      '【制作】干料 + 清水约 550ml，打至细腻并煮熟；保留豆渣，不加糖。',
      '【饮用】约 40℃ 温热，建议与水煮蛋等同食后一次喝完；睡前 2 小时勿饮。',
    ].join('\n'),
    notes: [
      `功效：${focus}`,
      extraNotes ? `\n${extraNotes}` : '',
      '',
      SOY_BREAKFAST_COMMON_RULES,
    ].filter(Boolean).join('\n'),
  };
}

