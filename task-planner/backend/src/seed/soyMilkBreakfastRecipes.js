function recipe({
  day,
  title,
  totalG,
  focus,
  ingredients,
  tags,
  extraNotes = '',
  eggCount = 2,
}) {
  const { calories, dryKcal, eggKcal, unknown } = estimateSoyBreakfastCalories(ingredients, { eggCount });
  if (unknown.length) {
    console.warn(`[soy-breakfast-d${day}] 未识别热量食材行:`, unknown);
  }

  return {
    title: `${title}（${totalG}g）`,
    mealType: '早餐',
    series: '豆浆早餐',
    planDay: day,
    templateKey: `soy-breakfast-d${day}`,
    prepMinutes: 25,
    calories,
    tags: `豆浆早餐,一人份,增肌控脂,周${['一', '二', '三', '四', '五', '六', '日'][day - 1]},${tags}`,
    ingredients: ingredients.join('\n'),
    steps: [
      '【前一晚】按上方干料称重；豆类/杂粮与山药茯苓莲子百合芡实分别用凉白开浸泡 ≥4 小时；燕麦片泡 2～4 小时。',
      '【早晨】倒掉全部泡水并沥干；黑芝麻/核桃/花生洗净即可。',
      '【制作】干料 + 清水约 550ml，打至细腻并煮熟；保留豆渣，不加糖。',
      '【配餐】水煮蛋煮熟；玉米蒸熟或全麦面包微烤；黄瓜/小番茄洗净；杏仁或核桃仁按克数装盘。',
      '【食用】先吃蛋与配餐几口 → 再温饮豆浆；一次吃完，不加糖饮料。',
    ].join('\n'),
    notes: [
      `功效：${focus}`,
      `营养：豆浆干料+配餐参考约 ${dryKcal} 千卡 + 水煮蛋 ${eggCount} 个约 ${eggKcal} 千卡 → 合计约 ${calories} 千卡（保留豆渣、不加糖）。`,
      '贴士：对齐增肌控脂——优质碳水、足量蛋白、蔬菜纤维、少量健康脂肪；拒绝油炸与含糖早餐。',
      extraNotes ? `\n${extraNotes}` : '',
      '',
      SOY_BREAKFAST_COMMON_RULES,
    ].filter(Boolean).join('\n'),
  };
}

