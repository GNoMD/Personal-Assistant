/**
 * AGA（雄激素性 / 脂溢性脱发倾向）× 增肌营养方案 — 系统食谱
 *
 * 定位：配合外用米诺地尔 + 外用非那雄胺期间的膳食支持；
 *       同时覆盖力量训练日的能量与蛋白需求。
 *
 * 营养师原则（参考，非处方）：
 * - 养发：每日足量优质蛋白（角蛋白原料）+ 铁/锌/铜适量食物来源 + 维C促铁吸收
 *         + 维E/Ω-3 抗炎 + B 族与卵磷脂；避免长期极端节食与酒精。
 * - 增肌：三餐蛋白落点约 35～55g；训练日总能量更高；碳水围绕训练；健康脂肪适量。
 * - 用药配合：外用米诺/非那无强制饮食禁忌；本方案强调「不亏营养、不暴渴、不过度油炸辛辣刺激头皮」；
 *             口服非那或合并其他用药者仍以医嘱为准。
 * - 乳糖友好：默认无乳糖奶/酸奶；尿酸关照：少内脏、不过量海鲜与浓汤，豆类控量。
 *
 * 7 日可轮换：早餐/午餐/晚餐/加餐/饮品各 7 道。
 * 三餐合计约 2200～2600 千卡（中等活动量男性参考），加餐与饮品按需叠加；可按体重±15% 调整主食。
 */

function dishes(lines) {
  return lines.join('\n');
}

function agaNotes({ focus, protein, calories, keyNutrients, tip, effects }) {
  return [
    `功效：${focus}`,
    `补什么：${keyNutrients}`,
    `作用：${effects || '提供毛发角蛋白合成原料，支持力量训练恢复，改善用药期间的整体营养状态。'}`,
    `营养：优质蛋白约 ${protein}；参考热量约 ${calories} 千卡/餐。`,
    tip ? `贴士：${tip}` : null,
    '说明：膳食支持毛囊营养与训练恢复，不能替代米诺地尔/非那雄胺或皮肤科治疗；特殊代谢疾病请遵医嘱调整。',
  ].filter(Boolean).join('\n');
}

function recipe(def) {
  return {
    prepMinutes: 30,
    tags: '',
    ...def,
    ingredients: Array.isArray(def.ingredients) ? dishes(def.ingredients) : def.ingredients,
    steps: Array.isArray(def.steps) ? def.steps.join('\n') : def.steps,
  };
}

