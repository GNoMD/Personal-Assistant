function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

function haversineMeters(lat1, lng1, lat2, lng2) {
  const R = 6371000;
  const toRad = (d) => (d * Math.PI) / 180;
  const dLat = toRad(lat2 - lat1);
  const dLng = toRad(lng2 - lng1);
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) * Math.sin(dLng / 2) ** 2;
  return 2 * R * Math.asin(Math.sqrt(a));
}

function formatDistance(meters) {
  if (!Number.isFinite(meters)) return '';
  if (meters < 1000) {
    const m = Math.max(50, Math.round(meters / 10) * 10);
    const walkMin = Math.max(1, Math.round(m / 80));
    return `约 ${m} 米 · 步行约 ${walkMin} 分钟`;
  }
  const km = (meters / 1000).toFixed(1);
  const walkMin = Math.round(meters / 80);
  return `约 ${km} 公里 · 步行约 ${walkMin} 分钟`;
}

function loadExisting() {
  if (!fs.existsSync(mapPath)) return {};
  try {
    return JSON.parse(fs.readFileSync(mapPath, 'utf8'));
  } catch {
    return {};
  }
}

function writeMap(map) {
  fs.writeFileSync(mapPath, JSON.stringify(map, null, 2), 'utf8');
  fs.writeFileSync(
    mapJsPath,
    `/* auto-generated by enrich-spot-parking.mjs */\nexport default ${JSON.stringify(map)};\n`,
    'utf8'
  );
}

function buildParkingQuery(spot) {
  return [spot.location || spot.address || '', spot.name, '停车场'].filter(Boolean).join('');
}

function defaultParking(spot) {
  const special = SPECIAL_RULES.find((r) => r.test(spot));
  if (special) {
    return {
      parkingName: special.name,
      hint: special.hint,
      walkNote: special.walkNote || '',
      distanceText: special.walkNote || '按导航前往停车场',
      mapQuery: buildParkingQuery(spot),
      source: 'special-rule',
    };
  }
  const cat = CATEGORY_PARKING[spot.category] || FALLBACK;
  return {
    parkingName: `${spot.name} · ${cat.name}`,
    hint: cat.hint,
    walkNote: '',
    distanceText: '打开地图搜索最近停车场',
    mapQuery: buildParkingQuery(spot),
    source: 'category-default',
  };
}

async function overpassNearestParking(lat, lng, radius = 1800) {
  const query = `
[out:json][timeout:30];
(
  node["amenity"="parking"](around:${radius},${lat},${lng});
  way["amenity"="parking"](around:${radius},${lat},${lng});
  relation["amenity"="parking"](around:${radius},${lat},${lng});
);
out center tags 40;
`.trim();

  const endpoints = [
    'https://overpass-api.de/api/interpreter',
    'https://overpass.kumi.systems/api/interpreter',
    'https://overpass.private.coffee/api/interpreter',
  ];

  let lastErr;
  for (let attempt = 0; attempt < 4; attempt += 1) {
    const endpoint = endpoints[attempt % endpoints.length];
    try {
      const res = await fetch(endpoint, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/x-www-form-urlencoded;charset=UTF-8',
          'User-Agent': UA,
        },
        body: `data=${encodeURIComponent(query)}`,
      });
      if (!res.ok) throw new Error(`overpass ${res.status}`);
      const data = await res.json();
      const elements = Array.isArray(data.elements) ? data.elements : [];
      const candidates = elements
        .map((el) => {
          const plat = el.lat ?? el.center?.lat;
          const plng = el.lon ?? el.center?.lon;
          if (typeof plat !== 'number' || typeof plng !== 'number') return null;
          const tags = el.tags || {};
          const name =
            tags.name ||
            tags['name:zh'] ||
            tags['name:en'] ||
            (tags.parking === 'multi-storey' ? '立体停车场' : '停车场');
          const meters = haversineMeters(lat, lng, plat, plng);
          return {
            parkingName: name,
            lat: plat,
            lng: plng,
            distanceMeters: Math.round(meters),
            fee: tags.fee || '',
            access: tags.access || '',
          };
        })
        .filter(Boolean)
        .filter((p) => !p.access || p.access === 'yes' || p.access === 'public' || p.access === 'customers')
        .sort((a, b) => a.distanceMeters - b.distanceMeters);

      return candidates[0] || null;
    } catch (err) {
      lastErr = err;
      await sleep(1500 + attempt * 2000);
    }
  }
  throw lastErr || new Error('overpass failed');
}

async function main() {
  const spots = Array.isArray(rich.spots) ? rich.spots : [];
  const map = FORCE ? {} : loadExisting();
  let seeded = 0;

  for (const spot of spots) {
    const addr = ADDRESS_MAP[spot.id] || {};
    const prev = map[spot.id] || {};
    const seededParking = defaultParking({
      ...spot,
      location: spot.location || addr.address || '',
      address: addr.address || spot.address || '',
    });

    // 特例（如鼓浪屿码头停车）优先；已有命名 OSM 结果则保留
    const keepOsm =
      !FORCE &&
      prev.source === 'osm' &&
      prev.parkingName &&
      prev.parkingName !== '停车场' &&
      typeof prev.lat === 'number' &&
      seededParking.source !== 'special-rule';

    map[spot.id] = keepOsm
      ? prev
      : {
          name: spot.name,
          provinceId: spot.provinceId,
          cityId: spot.cityId,
          parkingName: seededParking.parkingName,
          hint: seededParking.hint,
          walkNote: seededParking.walkNote || '',
          distanceText: seededParking.distanceText,
          mapQuery: seededParking.mapQuery,
          source: seededParking.source,
          updatedAt: new Date().toISOString(),
          ...(typeof addr.lat === 'number' && typeof addr.lng === 'number'
            ? { spotLat: addr.lat, spotLng: addr.lng }
            : {}),
        };
    seeded += 1;
  }

  writeMap(map);
  console.log(`seeded=${seeded} -> ${mapPath}`);

  if (!USE_OSM) return;

  const pending = spots.filter((s) => {
    const addr = ADDRESS_MAP[s.id] || {};
    if (typeof addr.lat !== 'number' || typeof addr.lng !== 'number') return false;
    if (SPECIAL_RULES.some((r) => r.test({ ...s, location: s.location || addr.address || '' }))) {
      return false;
    }
    const e = map[s.id];
    if (!e) return true;
    if (e.source === 'special-rule') return false;
    if (FORCE) return true;
    if (e.source === 'osm' && e.parkingName && !/^停车场$|^Parking$/i.test(e.parkingName)) return false;
    return true;
  });
  const list = LIMIT > 0 ? pending.slice(0, LIMIT) : pending;
  console.log(`osm enrich ${list.length}/${pending.length}`);

  let ok = 0;
  let fail = 0;
  for (let i = 0; i < list.length; i += 1) {
    const spot = list[i];
    const addr = ADDRESS_MAP[spot.id];
    if (map[spot.id]?.source === 'special-rule') {
      console.log(`[${i + 1}/${list.length}] ${spot.name} -> (keep special-rule)`);
      continue;
    }
    try {
      const nearest = await overpassNearestParking(addr.lat, addr.lng);
      if (nearest) {
        const named =
          nearest.parkingName && !/^停车场$|^Parking$/i.test(nearest.parkingName)
            ? nearest.parkingName
            : `${spot.name}最近停车场`;
        map[spot.id] = {
          ...map[spot.id],
          parkingName: named,
          lat: nearest.lat,
          lng: nearest.lng,
          distanceMeters: nearest.distanceMeters,
          distanceText: formatDistance(nearest.distanceMeters),
          walkNote: formatDistance(nearest.distanceMeters),
          fee: nearest.fee || '',
          mapQuery: `${named} ${spot.location || spot.name}`.trim(),
          source: 'osm',
          updatedAt: new Date().toISOString(),
          spotLat: addr.lat,
          spotLng: addr.lng,
        };
        ok += 1;
        console.log(
          `[${i + 1}/${list.length}] ${spot.name} -> ${named} (${nearest.distanceMeters}m)`
        );
      } else {
        console.log(`[${i + 1}/${list.length}] ${spot.name} -> (no parking in 1.8km, keep default)`);
      }
      if ((i + 1) % 10 === 0) writeMap(map);
      await sleep(1800 + Math.random() * 600);
    } catch (err) {
      fail += 1;
      console.warn(`[${i + 1}/${list.length}] FAIL ${spot.name}: ${err.message}`);
      await sleep(4000);
    }
  }

  writeMap(map);
  console.log(`osm done ok=${ok} fail=${fail}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
