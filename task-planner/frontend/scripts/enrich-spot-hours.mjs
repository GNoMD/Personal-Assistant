/**
 * 生成/补全景点开放时间索引 travelSpotHoursMap
 *
 *   node scripts/enrich-spot-hours.mjs              # 按类别写入默认开放时间
 *   node scripts/enrich-spot-hours.mjs --bing       # 上网检索更准的时间（慢）
 *   node scripts/enrich-spot-hours.mjs --bing --limit=50
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import rich from '../src/data/travelSpotsRich.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const mapPath = path.join(root, 'src/data/travelSpotHoursMap.json');
const mapJsPath = path.join(root, 'src/data/travelSpotHoursMap.js');
const UA = 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/122.0.0.0 Safari/537.36';

const args = Object.fromEntries(
  process.argv.slice(2).map((a) => {
    const m = a.match(/^--([^=]+)(?:=(.*))?$/);
    return m ? [m[1], m[2] ?? true] : [a, true];
  })
);
const USE_BING = Boolean(args.bing);
const LIMIT = args.limit ? Number(args.limit) : 0;
const FORCE = Boolean(args.force);

/** 类别默认开放时间（无检索结果时的合理参考，页面会提示以官方为准） */
const DEFAULT_HOURS = {
  博物馆: '周二至周日 09:00–17:00（常见周一闭馆，临闭馆前停止入场）',
  历史人文: '09:00–17:00（淡旺季可能调整，节假日请提前确认）',
  古镇街区: '街区全天可逛；付费景点多 08:30–17:30',
  宗教寺庙: '08:00–17:00（早晚课或法会期间部分区域可能暂闭）',
  主题乐园: '09:30–21:00（旺季延长，请以当日公告为准）',
  自然风光: '08:00–18:00（索道/观光车可能单独时段）',
  山地峡谷: '08:00–17:30（登山请预留下山时间，冬季可能缩短）',
  海滨湖泊: '全天可逛；付费景区多 08:00–18:00',
  公园休闲: '06:00–22:00（城市公园常见；收费园中园另计）',
  美食购物: '商街多 10:00–22:00；夜市更晚',
};

const FALLBACK = '开放时间请以景区/场馆官方当日公告为准';

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

function defaultHours(category) {
  return DEFAULT_HOURS[category] || FALLBACK;
}

function loadExisting() {
  if (!fs.existsSync(mapPath)) return {};
  try {
    return JSON.parse(fs.readFileSync(mapPath, 'utf8'));
  } catch {
    return {};
  }
}

function writeMap(map) {
  fs.writeFileSync(mapPath, JSON.stringify(map, null, 2), 'utf8');
  fs.writeFileSync(
    mapJsPath,
    `/* auto-generated by enrich-spot-hours.mjs */\nexport default ${JSON.stringify(map)};\n`,
    'utf8'
  );
}

function normalizeHours(raw) {
  let h = String(raw || '')
    .replace(/&quot;|&amp;/g, '')
    .replace(/\s+/g, ' ')
    .replace(/[：]/g, ':')
    .replace(/[－—~～到至]/g, '–')
    .trim();
  h = h.replace(/[。；;].*$/, '').trim();
  if (h.length < 5 || h.length > 60) return '';
  // must look like a time range or weekday schedule
  if (!/(\d{1,2}:\d{2}|上午|下午|全天|全年|周一|周二|周三|周四|周五|周六|周日|周一至|每日|每天)/.test(h)) {
    return '';
  }
  if (/(门票|价格|预约|攻略|怎么去|地址|电话|平方)/.test(h)) return '';
  return h;
}

function isPlausibleHours(hours) {
  const h = String(hours || '');
  if (!h || h === FALLBACK) return false;
  if (!/(\d{1,2}:\d{2}|全天|全年|周一|每日|每天|上午|下午)/.test(h)) return false;
  if (/(的岛屿|的城市|西北部|门票|攻略)/.test(h)) return false;
  return true;
}

async function bingSearch(query) {
  const url = `https://www.bing.com/search?q=${encodeURIComponent(query)}&setlang=zh-CN&cc=CN`;
  const res = await fetch(url, {
    headers: { 'User-Agent': UA, 'Accept-Language': 'zh-CN,zh;q=0.9', Accept: 'text/html' },
  });
  if (!res.ok) throw new Error(`bing ${res.status}`);
  return res.text();
}

function extractHours(html) {
  const text = String(html || '')
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/<[^>]+>/g, ' ')
    .replace(/\s+/g, ' ');

  const candidates = [];
  const patterns = [
    /开放时间[：:\s]*([^。；;<]{5,55})/g,
    /营业时间[：:\s]*([^。；;<]{5,55})/g,
    /参观时间[：:\s]*([^。；;<]{5,55})/g,
    /(\d{1,2}:\d{2}\s*[–\-~～到至]\s*\d{1,2}:\d{2}(?:[^。；;<]{0,20})?)/g,
    /((?:周一|周二|周三|周四|周五|周六|周日|周一至周日|每日|每天)[^。；;<]{0,8}\d{1,2}:\d{2}[^。；;<]{0,25})/g,
  ];
  for (const re of patterns) {
    for (const m of text.matchAll(re)) {
      const cleaned = normalizeHours(m[1] || m[0]);
      if (cleaned) candidates.push(cleaned);
    }
  }
  candidates.sort((a, b) => b.length - a.length);
  return candidates.find((c) => isPlausibleHours(c)) || '';
}

async function main() {
  const spots = Array.isArray(rich.spots) ? rich.spots : [];
  const map = FORCE ? {} : loadExisting();
  let seeded = 0;

  for (const spot of spots) {
    const prev = map[spot.id] || {};
    const fallback = defaultHours(spot.category);
    let openHours = prev.openHours || fallback;
    if (FORCE || !isPlausibleHours(openHours)) {
      openHours = fallback;
    }
    map[spot.id] = {
      name: spot.name,
      provinceId: spot.provinceId,
      cityId: spot.cityId,
      category: spot.category || '',
      openHours,
      note: '具体以景区/场馆官方当日公告为准，遇节假日、检修或预约制可能调整。',
      source: prev.source && isPlausibleHours(prev.openHours) ? prev.source : 'category-default',
      updatedAt: prev.updatedAt || new Date().toISOString(),
    };
    seeded += 1;
  }

  writeMap(map);
  console.log(`seeded=${seeded} -> ${mapPath}`);

  if (!USE_BING) return;

  const pending = spots.filter((s) => {
    const e = map[s.id];
    if (!e) return true;
    if (FORCE) return true;
    if (e.source === 'bing') return false;
    return e.source === 'category-default';
  });
  const list = LIMIT > 0 ? pending.slice(0, LIMIT) : pending;
  console.log(`bing enrich ${list.length}/${pending.length}`);

  let ok = 0;
  let fail = 0;
  for (let i = 0; i < list.length; i += 1) {
    const spot = list[i];
    const query = `"${spot.name}" ${spot.location || ''} 开放时间`.trim();
    try {
      const html = await bingSearch(query);
      const hours = extractHours(html);
      if (hours && isPlausibleHours(hours)) {
        map[spot.id] = {
          ...map[spot.id],
          openHours: hours,
          source: 'bing',
          updatedAt: new Date().toISOString(),
        };
        ok += 1;
      }
      if ((i + 1) % 20 === 0 || hours) {
        console.log(`[${i + 1}/${list.length}] ${spot.name} -> ${hours || '(keep default)'}`);
      }
      if ((i + 1) % 15 === 0) writeMap(map);
      await sleep(260 + Math.random() * 300);
    } catch (err) {
      fail += 1;
      console.warn(`[${i + 1}/${list.length}] FAIL ${spot.name}: ${err.message}`);
      await sleep(500);
    }
  }
  writeMap(map);
  console.log(`bing done ok=${ok} fail=${fail}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
