/**
 * 构建景点 → 相关行程索引 travelSpotPlanIndex
 * 仅保存 planId 列表，详情用 getTravelPlanById 解析省市/天数。
 *
 *   node scripts/build-spot-plan-index.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import rich from '../src/data/travelSpotsRich.js';
import { NATIONAL_TRAVEL_PLANS } from '../src/data/travel.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const jsonPath = path.join(root, 'src/data/travelSpotPlanIndex.json');
const jsPath = path.join(root, 'src/data/travelSpotPlanIndex.js');

function planSpotNames(plan) {
  if (plan?.spots?.length) return plan.spots.map((s) => s.name).filter(Boolean);
  if (plan?.days?.length) {
    return plan.days.flatMap((day) => (day.spots || []).map((s) => s.name)).filter(Boolean);
  }
  return [];
}

function main() {
  const spots = Array.isArray(rich.spots) ? rich.spots : [];
  /** @type {Map<string, string>} */
  const nameToId = new Map();
  for (const spot of spots) {
    if (!spot?.id || !spot?.name) continue;
    nameToId.set(`${spot.provinceId}::${spot.cityId}::${spot.name}`, spot.id);
  }

  /** @type {Record<string, string[]>} */
  const bySpotId = {};

  let planCount = 0;
  let linkCount = 0;

  for (const [provinceId, cities] of Object.entries(NATIONAL_TRAVEL_PLANS)) {
    for (const [cityId, byDuration] of Object.entries(cities || {})) {
      for (const plans of Object.values(byDuration || {})) {
        for (const plan of plans || []) {
          planCount += 1;
          const names = [...new Set(planSpotNames(plan))];
          for (const name of names) {
            const spotId = nameToId.get(`${provinceId}::${cityId}::${name}`);
            if (!spotId) continue;
            if (!bySpotId[spotId]) bySpotId[spotId] = [];
            if (bySpotId[spotId].includes(plan.id)) continue;
            bySpotId[spotId].push(plan.id);
            linkCount += 1;
          }
        }
      }
    }
  }

  const payload = {
    generatedAt: new Date().toISOString(),
    spotCount: Object.keys(bySpotId).length,
    linkCount,
    planCount,
    bySpotId,
  };

  fs.writeFileSync(jsonPath, JSON.stringify(payload), 'utf8');
  fs.writeFileSync(
    jsPath,
    `/* auto-generated by build-spot-plan-index.mjs */\nexport default ${JSON.stringify(payload)};\n`,
    'utf8'
  );
  console.log(
    `spots-with-plans=${payload.spotCount} links=${linkCount} plans-scanned=${planCount} -> ${jsPath}`
  );
}

main();
