/**
 * 生成/补全景点地址与坐标地图索引 travelSpotAddressMap
 *
 *   node scripts/enrich-spot-addresses.mjs           # 用 location + 5A 坐标快速生成
 *   node scripts/enrich-spot-addresses.mjs --bing    # 再上网检索更细地址（慢）
 *   node scripts/enrich-spot-addresses.mjs --bing --limit=50
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import rich from '../src/data/travelSpotsRich.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const mapPath = path.join(root, 'src/data/travelSpotAddressMap.json');
const mapJsPath = path.join(root, 'src/data/travelSpotAddressMap.js');
const source5aPath = path.join(__dirname, 'spot-seeds/source-5a.json');
const UA = 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/122.0.0.0 Safari/537.36';

const args = Object.fromEntries(
  process.argv.slice(2).map((a) => {
    const m = a.match(/^--([^=]+)(?:=(.*))?$/);
    return m ? [m[1], m[2] ?? true] : [a, true];
  })
);
const USE_BING = Boolean(args.bing);
const LIMIT = args.limit ? Number(args.limit) : 0;
const FORCE = Boolean(args.force);

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

function nameKey(name) {
  return String(name || '')
    .replace(/\s+/g, '')
    .replace(/[（(].*?[）)]/g, '')
    .replace(/(国家)?[345]A级?/g, '')
    .replace(/(旅游景区|风景名胜区|风景区|旅游区|景区)$/g, '')
    .toLowerCase();
}

function cleanAddress(raw) {
  let a = String(raw || '')
    .replace(/&quot;|&amp;|&lt;|&gt;/g, '')
    .replace(/\/图片.*$/g, '')
    .replace(/_百度百科.*$/g, '')
    .replace(/（国家5A级景区）/g, '')
    .replace(/\s+/g, '')
    .trim();
  a = a.split(/[，,。；;]/)[0];
  if (a.length < 6 || a.length > 48) return '';
  if (!/[省市区县镇路街巷号]/.test(a)) return '';
  if (/(平方公|面积|隔海|相望|开放时间|门票|图片|的岛屿|的城市|西北部|东南部沿)/.test(a)) return '';
  return a;
}

function isPlausibleAddress(address, spot) {
  const a = String(address || '');
  if (!a) return false;
  if (/(的岛屿|的城市|西北部|东南部|隔海相望|平方公|面积约)/.test(a)) return false;
  const loc = String(spot.location || '');
  if (loc && a === loc) return true;
  if (loc && a.startsWith(loc)) return true;
  // 允许「省市区 + 更细路名」
  if (loc && a.includes(loc.replace(/区$/, '')) && /[路街巷号镇乡]/.test(a)) return true;
  const cityHint = loc.match(/([\u4e00-\u9fff]{2,3})市/)?.[1];
  if (cityHint && a.includes(`${cityHint}市`) && !/(的岛屿|的城市)/.test(a)) return true;
  return false;
}

function buildMapQuery(spot) {
  return [spot.location || '', spot.name].filter(Boolean).join('');
}

function loadExisting() {
  if (!fs.existsSync(mapPath)) return {};
  try {
    return JSON.parse(fs.readFileSync(mapPath, 'utf8'));
  } catch {
    return {};
  }
}

function writeMap(map) {
  fs.writeFileSync(mapPath, JSON.stringify(map, null, 2), 'utf8');
  fs.writeFileSync(
    mapJsPath,
    `/* auto-generated by enrich-spot-addresses.mjs */\nexport default ${JSON.stringify(map)};\n`,
    'utf8'
  );
}

function load5aCoordIndex() {
  /** @type {Map<string, {lat:number,lng:number,rawName:string}>} */
  const idx = new Map();
  if (!fs.existsSync(source5aPath)) return idx;
  const rows = JSON.parse(fs.readFileSync(source5aPath, 'utf8'));
  for (const row of rows || []) {
    if (!row?.location || !row?.name) continue;
    const [lng, lat] = String(row.location).split(',').map(Number);
    if (!Number.isFinite(lat) || !Number.isFinite(lng)) continue;
    const entry = { lat, lng, rawName: row.name };
    idx.set(nameKey(row.name), entry);
    // also without city/province prefixes
    const short = String(row.name)
      .replace(/^.+?(省|市)/, '')
      .replace(/^.+?市/, '');
    if (short) idx.set(nameKey(short), entry);
  }
  return idx;
}

async function bingSearch(query) {
  const url = `https://www.bing.com/search?q=${encodeURIComponent(query)}&setlang=zh-CN&cc=CN`;
  const res = await fetch(url, {
    headers: { 'User-Agent': UA, 'Accept-Language': 'zh-CN,zh;q=0.9', Accept: 'text/html' },
  });
  if (!res.ok) throw new Error(`bing ${res.status}`);
  return res.text();
}

function extractAddress(html, spot) {
  const text = String(html || '')
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/<[^>]+>/g, ' ')
    .replace(/\s+/g, ' ');
  const city = spot.location || '';
  const candidates = [];
  const patterns = [
    /地址[：:\s]*([^\s。；;]{6,50})/g,
    /位于([^\s。；;]{6,50})/g,
  ];
  if (city) {
    patterns.push(new RegExp(`(${city.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}[^\\s。；;]{0,30})`, 'g'));
  }
  for (const re of patterns) {
    for (const m of text.matchAll(re)) {
      const cleaned = cleanAddress(m[1] || m[0]);
      if (cleaned) candidates.push(cleaned);
    }
  }
  // prefer longest plausible
  candidates.sort((a, b) => b.length - a.length);
  return candidates[0] || '';
}

async function main() {
  const spots = Array.isArray(rich.spots) ? rich.spots : [];
  const map = loadExisting();
  const coordIdx = load5aCoordIndex();
  let seeded = 0;
  let coordHit = 0;

  for (const spot of spots) {
    const prev = FORCE ? {} : (map[spot.id] || {});
    const mapQuery = buildMapQuery(spot);
    let lat = typeof prev.lat === 'number' ? prev.lat : undefined;
    let lng = typeof prev.lng === 'number' ? prev.lng : undefined;

    const nk = nameKey(spot.name);
    const hit = coordIdx.get(nk);
    if (hit && (lat == null || lng == null || FORCE)) {
      lat = hit.lat;
      lng = hit.lng;
      coordHit += 1;
    }

    let address = prev.address || spot.location || mapQuery;
    if (FORCE || !isPlausibleAddress(address, spot)) {
      address = spot.location || mapQuery;
    }

    map[spot.id] = {
      name: spot.name,
      provinceId: spot.provinceId,
      cityId: spot.cityId,
      address,
      mapQuery,
      lat,
      lng,
      source: (!FORCE && prev.source && isPlausibleAddress(prev.address, spot))
        ? prev.source
        : (lat != null ? 'mct-5a-coord+location' : 'location'),
      updatedAt: new Date().toISOString(),
    };
    seeded += 1;
  }

  writeMap(map);
  console.log(`seeded=${seeded} coordHit≈${coordHit} -> ${mapPath}`);

  if (!USE_BING) return;

  const pending = spots.filter((s) => {
    const e = map[s.id];
    if (!e) return true;
    if (FORCE) return true;
    // already bing-enriched
    if (e.source?.includes('bing')) return false;
    // keep searching if address is only coarse location
    return !e.address || e.address === s.location || e.address.length < 10;
  });
  const list = LIMIT > 0 ? pending.slice(0, LIMIT) : pending;
  console.log(`bing enrich ${list.length}/${pending.length}`);

  let ok = 0;
  let fail = 0;
  for (let i = 0; i < list.length; i += 1) {
    const spot = list[i];
    const query = `"${spot.name}" ${spot.location || ''} 地址`.trim();
    try {
      const html = await bingSearch(query);
      const address = extractAddress(html, spot);
      if (address && isPlausibleAddress(address, spot)) {
        // 只在比现有地址更细时覆盖
        const prevAddr = map[spot.id]?.address || '';
        const better = address.length > prevAddr.length || address !== spot.location;
        if (better) {
          map[spot.id] = {
            ...map[spot.id],
            address,
            source: map[spot.id]?.lat != null ? 'bing+coord' : 'bing',
            updatedAt: new Date().toISOString(),
          };
          ok += 1;
        }
      }
      if ((i + 1) % 20 === 0 || (address && isPlausibleAddress(address, spot))) {
        console.log(`[${i + 1}/${list.length}] ${spot.name} -> ${(address && isPlausibleAddress(address, spot)) ? address : '(keep)'}`);
      }
      if ((i + 1) % 15 === 0) writeMap(map);
      await sleep(280 + Math.random() * 320);
    } catch (err) {
      fail += 1;
      console.warn(`[${i + 1}/${list.length}] FAIL ${spot.name}: ${err.message}`);
      await sleep(600);
    }
  }
  writeMap(map);
  console.log(`bing done ok=${ok} fail=${fail}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
